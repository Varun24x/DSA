package ac_conditionalsloops.lecture;

public class Code_02 {
    public static void main(String[] args) {
        
        // Loops
        for(int i = 1 ; i <= 5 ; i++){
            System.out.print(i + " ");
        }
        System.out.println();

        // While loop
        int count = 1;
        while(count <= 5){
            System.out.print(count + " ");
            count++;
        }
        System.out.println();

        // Do-while loop
        do { 
            System.out.println("Hello gaizz");
        } while (false);
    }
    
}
